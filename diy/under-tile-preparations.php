      <h1>Under Tile Preparations</h1>
      <p>Under tile heating is a quick and easy application. Most installations have the first stage completed within a day. There are a few preparation procedures that need to be in place to ensure a smooth application of our product. Under tile heating is laid closer to the end of your building process just before tiling. For example in bathrooms the under tile heating is installed once the floor preparations, jibing, waterproofing, pre-wire and fixtures and fittings are installed or marked out(for kitchens we prefer the cabinetry to be in place).</p>
      <p>The pre-wire(draw wire) will need to be in place before the first part of the installation and the circuit wiring will need to be in place before the final fit of the thermostat(s). See the instructions on our specific Under Tile preparation page for more details.</p>
      <h2>Work required to be done by other trades for Under Tile Heating:</h2>
      <p>We require the following work to be carried out by other trades to ensure the heating is installed and operates correctly.</p>
      <div class="line-break"></div>
      
      <h3>Builders Work</h3>
      <h4>Wooden Floors:</h4>
      <p>These should be made water tight or covered with a layer of tile and slate underlay. The floor is to be clean and free of any cracks, holes or sharp protrusions. If you decide to lay the tiles straight on to the particle board it has to be sanded clean.
</p>
      <h4>Concrete Floors:</h4>
      <p>The floor is to be clean, level and free of any cracks, holes or sharp protrusions.</p> 
      <h4>Fixtures & Fittings:</h4>
      <p>All fixtures and fittings are to be marked out or installed before the heating installation. If floor mounted door stops are to be used please mark out on the floor to avoid damage to the heating.</p>
      <div class="line-break"></div> 
      <h3>Electrical Work</h3>
      <p><span class="bold">This is taken to include but not limited to:</span></p>
      <p>Running all the circuit wiring for each heating zone. Please note that any loads over 1000 watts will need a relay and a control circuit back to the switch board. The electrician will have to connect the power to the thermostat(s) once they have been fitted by Heatwell Ltd.</p>
      <p>Supply and fitting the RCD circuit breakers to the switchboard. This is a condition of your guarantee.</p>
      <p>Supply and installing of the draw wires from the thermostat position to the floor. This is done so the element tails can be drawn up at a later date. Please refer to our wiring diagram for draw wire instructions (included).</p>
      <p><em>All flush boxes for thermostats should be mounted vertically so an automatic thermostat can be fitted.</em></p>
      <div class="line-break"></div>
      <h3>All Trades</h3>
      <p>No one is to walk on the heating elements except the tiler.</p>
      <div class="line-break"></div>
      <h3>Tiler</h3>
      <p>We require the use of 2 pot flexible tile adhesive and a latex based grout.</p>
      <p>Tiling is commenced 24 hours after the elements are laid. The tiler should be warned of the dangers in using sharp tools, which may damage this type of installation.</p>
      <p><span class="bold">Specification For Wiring Under Tile Heating</span></p>
      <ol>
        <li>For manual thermostats mount flush box above second nog or at approximately 1680mm above floor level to ensure a correct room temperature reading.</li>
        <li>For automatic thermostats mount flush box at any desired height as they utilise a floor probe.</li>
        <li>The flush box should be mounted vertically to allow for a programmable thermostat.</li>
        <li>Holes for the draw wire should be a "Minimum of 25mm" diameter to ensure that no jamming occurs while pulling wires up from the floor.</li>
        <li>DO NOT use TPS cable as a draw wire, single core wire only to be used.</li>
        <li>Check bottom plate out to minimum dimension of 50mm wide and 25mm depth.</li>
        <li>Nail or staple draw wire to floor to stop it from being removed or lost during construction.</li>
        <li>It is imperative that the draw wire be free to move and not be jammed by any wall tiles, wall insulation, nogs or skirting.</li>
      </ol>
